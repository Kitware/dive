<script>
import { mapState, mapMutations, mapActions } from 'vuex';
import { FileManager } from '@girder/components/src/components/Snippet';
import { getLocationType } from '@girder/components/src/utils';

import Export from '@/components/Export.vue';
import Upload from '@/components/Upload.vue';
import NavigationBar from '@/components/NavigationBar.vue';
import { videoFilesRegEx, webFriendlyImageRegEx, imageFilesRegEx } from '@/constants';
import { getPathFromLocation, getLocationFromRoute } from '@/utils';
import {
  runVideoConversion,
  deleteResources,
  setMetadataForItem,
  runImageConversion,
} from '@/common/viame.service';

export default {
  name: 'Home',
  components: {
    Export,
    FileManager,
    Upload,
    NavigationBar,
  },
  inject: ['notificationBus'],
  data: () => ({
    location_: null,
    uploaderDialog: false,
    selected: [],
    uploading: false,
  }),
  computed: {
    ...mapState(['location', 'pipelines']),

    location: {
      get() {
        return this.location_;
      },
      set(value) {
        this.location_ = value;
        const newPath = getPathFromLocation(value);
        if (this.$route.path !== newPath) {
          this.$router.push(newPath);
        }
        this.setLocation(value);
      },
    },
    shouldShowUpload() {
      return (
        this.location
        && getLocationType(this.location) === 'folder'
        && !this.selected.length
      );
    },
    selectedEligibleClips() {
      return this.selected.filter(
        (model) => model._modelType === 'folder' && model.meta && model.meta.annotate,
      );
    },
    pipelinesRunnable() {
      return this.selectedEligibleClips.length < 1 || !this.pipelines.length;
    },
    exportTarget() {
      let { selected } = this;
      if (selected.length === 1) {
        [selected] = selected;
        if (selected._modelType !== 'folder') {
          return null;
        }
        return {
          title: selected.name,
          folderId: selected._id,
          folderType: selected.meta && selected.meta.type,
          isViameFolder: selected.meta && selected.meta.annotate,
        };
      }
      return null;
    },
  },
  watch: {
    uploading(newval) {
      if (!newval) {
        this.$refs.fileManager.$refs.girderBrowser.refresh();
      }
    },
  },
  created() {
    this.location_ = getLocationFromRoute(this.$route);
    this.setLocation(this.location_);
    this.fetchPipelines();
    this.notificationBus.$on('message:job_status', this.handleNotification);
  },
  beforeDestroy() {
    this.notificationBus.$off('message:job_status', this.handleNotification);
  },
  beforeRouteUpdate(to, from, next) {
    this.location_ = getLocationFromRoute(to);
    next();
  },
  methods: {
    ...mapMutations(['setLocation', 'setPipelines']),
    ...mapActions(['fetchPipelines']),
    handleNotification() {
      this.$refs.fileManager.$refs.girderBrowser.refresh();
    },
    isAnnotationFolder(item) {
      // TODO: update to check for other info
      return item._modelType === 'folder' && item.meta.annotate;
    },
    async deleteSelection() {
      const result = await this.$prompt({
        title: 'Confirm',
        text: 'Do you want to delete selected items?',
        confirm: true,
      });
      if (!result) {
        return;
      }
      await deleteResources(this.selected);
      this.$refs.fileManager.$refs.girderBrowser.refresh();
      this.selected = [];
    },
    dragover() {
      if (this.shouldShowUpload) {
        this.uploaderDialog = true;
      }
    },
    uploaded(uploads) {
      this.uploaderDialog = false;

      // Check if any transcoding should be done
      const transcodes = uploads.filter(({ results, folder }) => {
        const videoTranscodes = results
          .filter(({ name }) => videoFilesRegEx.test(name))
          .map(({ itemId }) => runVideoConversion(itemId));
        const imageTranscodes = results
          .filter(({ name }) => !webFriendlyImageRegEx.test(name) && imageFilesRegEx.test(name));

        if (imageTranscodes.length > 0) {
          runImageConversion(folder._id);
        }
        return videoTranscodes.concat(...imageTranscodes).length > 0;
      });

      if (transcodes.length) {
        this.$snackbar({
          text: `Transcoding started on ${transcodes.length} clip${
            transcodes.length > 1 ? 's' : ''
          }`,
          timeout: 4500,
          button: 'View',
          callback: () => {
            this.$router.push('/jobs');
          },
        });
      }

      // promote csv files to as its own result item
      uploads.forEach(({ folder, results }) => {
        const csvFiles = results.filter((result) => result.name.endsWith('.csv'));
        csvFiles.forEach((csvFile) => setMetadataForItem(
          csvFile.itemId,
          {
            detection: folder._id,
          },
        ));
      });
    },

    async runPipelineOnSelectedItem(pipeline) {
      const clips = this.selectedEligibleClips;
      await Promise.all(
        this.selectedEligibleClips.map((item) => this.runPipeline(item._id, pipeline)),
      );
      this.$snackbar({
        text: `Started pipeline on ${clips.length} clip${
          clips.length ? 's' : ''
        }`,
        timeout: 6000,
        immediate: true,
        button: 'View',
        callback: () => {
          this.$router.push('/jobs');
        },
      });
    },
  },
};
</script>

<template>
  <v-content>
    <NavigationBar />
    <v-container fill-height>
      <v-row
        class="fill-height"
        no-gutters
      >
        <v-col :cols="12">
          <FileManager
            ref="fileManager"
            v-model="selected"
            :new-folder-enabled="!selected.length"
            selectable
            :location.sync="location"
            @dragover.native="dragover"
          >
            <template #headerwidget>
              <v-menu offset-y>
                <template v-slot:activator="{ on }">
                  <v-btn
                    text
                    small
                    :disabled="pipelinesRunnable"
                    v-on="on"
                  >
                    <v-icon
                      left
                      color="accent"
                    >
                      mdi-pipe
                    </v-icon>
                    Run pipeline
                  </v-btn>
                </template>
                <v-list>
                  <v-list-item
                    v-for="pipeline in pipelines"
                    :key="pipeline"
                    @click="runPipelineOnSelectedItem(pipeline)"
                  >
                    <v-list-item-title>{{ pipeline }}</v-list-item-title>
                  </v-list-item>
                </v-list>
              </v-menu>
              <export
                v-if="exportTarget"
                v-bind="exportTarget"
              />
              <v-btn
                v-if="selected.length"
                class="ma-0"
                text
                small
                @click="deleteSelection"
              >
                <v-icon
                  left
                  color="accent"
                  class="mdi-24px mr-1"
                >
                  mdi-delete
                </v-icon>
                Delete
              </v-btn>
              <v-dialog
                v-if="shouldShowUpload"
                v-model="uploaderDialog"
                max-width="800px"
                :persistent="uploading"
              >
                <template #activator="{on}">
                  <v-btn
                    class="ma-0"
                    text
                    small
                    v-on="on"
                  >
                    <v-icon
                      left
                      color="accent"
                    >
                      mdi-file-upload
                    </v-icon>
                    Upload
                  </v-btn>
                </template>
                <Upload
                  :location="location"
                  :uploading.sync="uploading"
                  @uploaded="uploaded"
                />
              </v-dialog>
            </template>
            <template #row-widget="{item}">
              <v-btn
                v-if="isAnnotationFolder(item)"
                class="ml-2"
                x-small
                color="primary"
                :to="{ name: 'viewer', params: { datasetId: item._id } }"
                @click.stop="openClip(item)"
              >
                Annotate
              </v-btn>
            </template>
          </FileManager>
        </v-col>
      </v-row>
    </v-container>
  </v-content>
</template>
