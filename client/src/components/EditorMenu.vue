<script lang="ts">
import Vue, { PropType } from 'vue';
import { Ref } from '@vue/composition-api';
import { EditAnnotationTypes } from '@/components/layers/EditAnnotationLayer';

export default Vue.extend({
  name: 'EditorMenu',
  props: {
    editingTrack: {
      type: Object as PropType<Ref<boolean>>,
      required: true,
    },
    annotationState: {
      type: Object as PropType<Ref<{
        visible: EditAnnotationTypes[];
        editing: EditAnnotationTypes;
      }>>,
      required: true,
    },
  },

  data() {
    return {
      buttons: [
        { id: 'rectangle', title: 'Bounds', icon: 'mdi-vector-square' },
        { id: 'polygon', title: 'Polygon', icon: 'mdi-vector-polygon' },
      ],
    };
  },

  computed: {
    config() {
      if (this.editingTrack.value) {
        return {
          color: 'primary',
          class: ['primary'],
          text: 'Edit',
          icon: 'mdi-pencil',
          model: 'editing',
          multiple: false,
        };
      }
      return {
        color: 'grey',
        class: ['grey', 'darken-2'],
        text: 'View',
        icon: 'mdi-eye',
        model: 'visible',
        multiple: true,
      };
    },
  },
});
</script>

<template>
  <v-row>
    <v-divider vertical />
    <v-col class="d-flex align-center py-0">
      <span :class="['mr-1', 'px-2', 'py-1', 'modechip', ...config.class ]">
        <v-icon class="pr-1">
          {{ config.icon }}
        </v-icon>
        <span class="text-subtitle-2">
          {{ config.text }} mode
        </span>
      </span>
      <v-btn-toggle
        :value="annotationState.value[config.model]"
        :multiple="config.multiple"
        :mandatory="!config.multiple"
        group
        @change="$emit('set-annotaiton-state', { [config.model]: $event })"
      >
        <v-btn
          v-for="button in buttons"
          :key="button.id"
          :value="button.id"
          outlined
          icon
          active-class="active-editor-menu-button"
          style="border-radius: 5px;"
          :color="config.color"
        >
          <v-icon>{{ button.icon }}</v-icon>
        </v-btn>
      </v-btn-toggle>
    </v-col>
    <v-divider vertical />
  </v-row>
</template>

<style scoped>
.modechip {
  border-radius: 16px;
  width: 120px;
}
</style>
